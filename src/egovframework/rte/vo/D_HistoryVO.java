package egovframework.rte.vo;

public class D_HistoryVO {
	
	private String docno;
	private int docversion;
	private String histcontent;
	private String histkind;
	private String projectcode;
	private String projecttype;
	private String projectname;
	private String created;
	private String creator;
	
	public String getDocno() {
		return docno;
	}
	public void setDocno(String docno) {
		this.docno = docno;
	}
	public int getDocversion() {
		return docversion;
	}
	public void setDocversion(int docversion) {
		this.docversion = docversion;
	}
	public String getHistcontent() {
		return histcontent;
	}
	public void setHistcontent(String histcontent) {
		this.histcontent = histcontent;
	}
	public String getHistkind() {
		return histkind;
	}
	public void setHistkind(String histkind) {
		this.histkind = histkind;
	}
	public String getProjectcode() {
		return projectcode;
	}
	public void setProjectcode(String projectcode) {
		this.projectcode = projectcode;
	}
	public String getProjecttype() {
		return projecttype;
	}
	public void setProjecttype(String projecttype) {
		this.projecttype = projecttype;
	}
	public String getProjectname() {
		return projectname;
	}
	public void setProjectname(String projectname) {
		this.projectname = projectname;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}

}
